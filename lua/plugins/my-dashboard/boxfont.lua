local function multilineStringToTable(str)
	local lines = {}
	for s in str:gmatch("[^\r\n]+") do
		table.insert(lines, s)
	end
	return lines
end

BOXFONT.toBoxFont = function(str)
	local numLines = 6
	local stringTable = {}

	for i = 1, numLines do
		stringTable[i] = ""
	end

	for j = 1, #str do
		local char = str:sub(j, j)
		local blockCharLines = multilineStringToTable(boxFont[char])
		local numBlockLines = #blockCharLines

		if not (numBlockLines == numLines) then
			print("BAD CHARACTER: [" .. char .. "]: Num lines of char is " .. numBlockLines .. " !== " .. numLines)
		else
			for i, l in ipairs(blockCharLines) do
				stringTable[i] = stringTable[i] .. l
			end
		end
	end

	local result = ""
	for _, l in ipairs(stringTable) do
		result = result .. l .. "\n"
	end
	return result:sub(1, -2)
end

return BOXFONT
